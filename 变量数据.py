# 变量由标识，类型和值三部分组成，其中标识可以使用内置函数id()来获取，类型通过type()来获取

name = 'python'
print('name变量地址：', id(name))
print('name变量类型:', type(name))

# python整数默认是十进制，其他还有二进制，八进制，十六进制

print('二进制', 0b1010001)
print('八进制', 0o7621304)
print('十六进制', 0xA98FE2)

