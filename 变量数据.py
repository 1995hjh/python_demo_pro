# 数据类型转换
# str类型与int类型连接时报错，解决方案是类型转换
# str(): 将其他数据类型转换为字符串
# int():将其他类型转换为整数  【1、文字类和小数类字符串，无法转换为整数；2、浮点数转换为整数：舍掉小数部分，只取整数】
# float():将其他数据类型转换为浮点数 【1、文字类无法转成浮点数；2、整数转换为浮点数，末尾为.0】

name = '张三'
age = 20

print('他是' + name + '，今年' + str(age) + '岁')


