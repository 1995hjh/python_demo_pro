# 标准算术运算符 + - * / //[整除]
# 取余运算符 %
# 幂运算 **
# 整除运算一正一负的时候向下取整
# 取余（一正一负要公式  余数=被除数-除数*商）

print(9 // 4)  # 2
print(-9 // -4)  # 2
print(-9 // 4)  # -3
print(9 // -4)  # -3

print(9 % 4)  # 1
print(-9 % -4)  # -1
print(9 % -4)  # -3  9-(-4)*(-3)
print(-9 % 4)  # 3  -9 - 4 * (-3) = 3

print('-------------------链式赋值------------------------')
