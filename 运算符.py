print('-------------------比较运算符------------------------')

# 比较运算符 结果为bool类型
# >, < ,>= ,<= ,!= ,==  , is   , is not
# == 比较的是两个变量的值
# is,is not 比较的是两个变量的标识
# 当两个变量的值相同，且变量是基础类型是，python将引用同一个标识
m = 10
n = 10
p = 11
print(m, id(m))
print(n, id(n))
print(p, id(p))
print(m is n) # True
print(m is not p) # True
arr1 = [10, 20, 30]
arr2 = [10, 20, 30]
print('arr1', id(arr1))
print('arr2', id(arr2))
print(arr1 is arr2) # False


